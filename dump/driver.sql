	CREATE TABLE IF NOT EXISTS DRIVER(
		`DriverId` 		INT NULL AUTO_INCREMENT,
		`DriverName` 	VARCHAR(20) NOT NULL,
		`Address`		VARCHAR(70)		NOT NULL,
		`Cug`			VARCHAR(10),
		`MobileNo`		VARCHAR(10),
		`Salary`		INT(5),
		`DateOfJoining`	DATE,
		CONSTRAINT DRIVER_PK		PRIMARY KEY(`DriverId`),
		CONSTRAINT DRIVER_CHECK_ID	CHECK(`DriverId` like  'D[0-9][0-9][0-9]'),
		CONSTRAINT DRIVER_NAME_INITCAP	CHECK(`DriverName`=INITCAP(`DriverName`)),
		CONSTRAINT DRIVER_CHECK_SALARY	CHECK(`Salary` >1000),
		CONSTRAINT DRIVER_CHECK_MOB		CHECK(`MobileNo` like '7%'),
		CONSTRAINT DRIVER_CHECK_CUG		CHECK(`Cug` like '9%'or `Cug` like '8%' or `Cug` like '7%'),
		CONSTRAINT DRIVER_CHECK_CUG_LENGTH	CHECK(length(`Cug`)>9 and length(`Cug`)<11),
		CONSTRAINT DRIVER_UNIQUE_CUG	UNIQUE(`Cug`)
	);
